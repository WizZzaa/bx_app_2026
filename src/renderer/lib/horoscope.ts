// FNV-1a хэш для детерминированного выбора по строке-дате
function hashStr(s: string): number {
  let h = 0x811c9dc5;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 0x01000193);
  }
  return h >>> 0;
}

function pick<T>(arr: T[], seed: number): T {
  return arr[seed % arr.length];
}

export interface DailyHoroscope {
  mood: string;
  advice: string;
  luckyAccount: string;
  color: string;
  warning: string;
}

/** Предсказание на конкретную дату (по умолчанию — сегодня). */
export function getHoroscope(date = new Date()): DailyHoroscope {
  const key = `${date.getFullYear()}-${date.getMonth()}-${date.getDate()}`;
  const base = hashStr(key);
  return {
    mood: pick(moods, base),
    advice: pick(advices, base >> 3),
    luckyAccount: pick(luckyAccounts, base >> 7),
    color: pick(colors, base >> 11),
    warning: pick(warnings, base >> 15),
  };
}

